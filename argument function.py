def sum_numbers(*args):
    return sum(args)
print("1, 2, 3 = ", sum_numbers(1, 2, 3))                      
print("8, 20, 2 = ", sum_numbers(8, 20, 2))                    
print("12.5, 3.147, 98.1 = ", sum_numbers(12.5, 3.147, 98.1))  
print("1.1, 2.2, 5.5 = ", sum_numbers(1.1, 2.2, 5.5))          
